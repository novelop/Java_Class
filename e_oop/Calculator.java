package e_oop;

public class Calculator {
	
	double sum (double a, double b) {
		return a + b;
	}
	
	double mov(double a,double b) {
		return a - b;
	}
	
	double mok(double a, double b) {
		return a / b;
	}
	
	double gob(double a, double b) {
		return a * b;
	}
	
	double nam(double a, double b) {
		return a % b;
	}
	
	
	
}
